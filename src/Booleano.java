public class Booleano {
    private boolean booleano;
    public Booleano(){
        this.booleano=false;
    }
    public Booleano(boolean booleano){
        this.booleano=booleano;
    }
    public boolean getValor(){
        return this.booleano;
    }
    public void setValor(boolean booleano) {
        this.booleano = booleano;
    }
}
